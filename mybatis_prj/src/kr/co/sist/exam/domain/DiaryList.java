package kr.co.sist.exam.domain;

public class DiaryList {
	private int num;
	private String subject, writer, evt_date, w_date;
	
	public int getNum() {
		return num;
	}
	public String getSubject() {
		return subject;
	}
	public String getWriter() {
		return writer;
	}
	public String getEvt_date() {
		return evt_date;
	}
	public String getW_date() {
		return w_date;
	}
	
	
}
